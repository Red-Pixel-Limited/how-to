final class LiteralsDemo {

    public static void run() {
        int hex = 0xFF;
        int oct = 011;
        int bin = 0b1100;

        System.out.println("Hex: " + hex);     // 255
        System.out.println("Octal: " + oct);   // 9
        System.out.println("Binary: "  + bin); // 12
    }
}
